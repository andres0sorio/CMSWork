//=============================================================================
// Standard constructor, initializes variables
//=============================================================================
TTUInput::TTUInput(  ) {
  
  for(int i=0; i < 12; ++i) {
    for (int j=0; j < 6; ++j ) {
      input_sec[i].set(j,false);
    }
  }
}
//=============================================================================
// Destructor
//=============================================================================
TTUInput::~TTUInput() {} 

//=============================================================================

void TTUInput::reset() 
{
  for(int i=0; i < 12; ++i) {
    for (int j=0; j < 6; ++j ) {
      input_sec[i].set(j,false);
    }
  }
}
